def _parse_severity(s: str) -> Severity:
    try:
        return Severity(s.lower())
    except ValueError:
        return Severity.PREFERRED


def _check_import_present(source: str, helper_name: str) -> bool:
    """Return True if train.py imports helper_name from _openresearch_curated."""
    # Match: from _openresearch_curated import <identifiers>
    # The helper_name must appear as a whole identifier token (word boundary).
    pattern = re.compile(
        r"^from\s+_openresearch_curated\s+import\s+(.+)$",
        re.MULTILINE,
    )
    for m in pattern.finditer(source):
        imports_str = m.group(1)
        # Split on commas and strip each identifier
        imported_names = {tok.strip() for tok in imports_str.split(",")}
        if helper_name in imported_names:
            return True
    return False


def _extract_helper_body(module_source: str, helper_name: str) -> str | None:
    """Extract the function body text for helper_name from module_source.

    Returns the raw source of the def block (from 'def <name>' to EOF or
    next top-level def/class). Returns None if not found.
    """
    lines = module_source.splitlines(keepends=True)
    start_idx: int | None = None
    for i, line in enumerate(lines):
        if re.match(rf"^def\s+{re.escape(helper_name)}\s*\(", line):
            start_idx = i
            break
    if start_idx is None:
        return None

    # Collect lines until next top-level def/class or EOF.
    body_lines: list[str] = []
    for line in lines[start_idx:]:
        body_lines.append(line)
        # Stop at next top-level definition (not the first line).
        if len(body_lines) > 1 and re.match(r"^(def|class)\s+", line):
            body_lines.pop()  # don't include the next definition's header
            break

    return "".join(body_lines).rstrip()
